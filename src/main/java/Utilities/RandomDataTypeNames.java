package Utilities;

/**
 * Enum representing different types of random data that can be generated.
 */
public enum RandomDataTypeNames {
    FIRSTNAME,   // Represents a first name
    LASTNAME,    // Represents a last name
    FULLNAME,    // Represents a full name
    COUNTRY,     // Represents a country name
    CITYNAME,    // Represents a city name
    EMAIL,       // Represents an email address
    IP_ADDRESS,  // Represents an IP address
}
